Терміни, що використовуються у правилах
1. Багаж – речі пасажира, призначені для перевезення у багажному відділенні автобуса.
1.2. Ручна поклажа – невеликі речі особистого користування, що не перевищують розміри 50 х 40 х 20 см і вага 5 кг (верхній одяг, жіноча сумка, пляшка води, парасолька, ноутбук і т.д.).
Загальні правила
1. При перевезенні багажу в багажному відділенні автобуса перевізник несе відповідальність
лише за наявність одиниці багажу, але не гарантує збереження його цілісності чи
працездатності. Перевізник не бере на себе відповідальність за незначне або поверхневе
