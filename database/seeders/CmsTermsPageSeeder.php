Ручну поклажу, яка має бути в салоні мікроавтобуса у пасажира. Ручна поклажа
знаходиться під ногами або на багажній полиці. Заборонено розміщувати ручну поклажу у
проході салону мікроавтобуса.
До 3-х (трьох) одиниць багажу, загальна вага яких не перевищує 40 кг.
Обсяг ділиться так:

 Пасажирам – дітям: дитячий візок (тільки у складеному вигляді), за відсутності іншого
багажу.
Багаж, за який треба доплачувати
1. Додатковий багаж приймається до перевезення лише за наявності вільного місця у
