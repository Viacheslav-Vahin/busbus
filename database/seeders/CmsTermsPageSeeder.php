пошкодження, такі як порізи, подряпини, заломи чи плями, що з’явилися під час звичайної
амортизації та використання під час поїздки. За безпеку ручної поклажі відповідає сам
пасажир.
2.2. Здаючи багаж, пасажир повинен мати на увазі, що протягом усього рейсу доступ до
особистих речей у багажному відділенні закрито.
2.4. За збереження ручної поклажі під час рейсу, у тому числі під час зупинок та кордонів,
відповідає сам Пасажир.
2.5. З метою безпеки Перевізник має право попросити пред’явити вміст багажу. У разі відмови
Перевізник має право не допустити цього пасажира до поїздки.
2.6. Багаж без пасажира не перевозиться.
Безкоштовне перевезення багажу
